import java.util.Scanner;

public class Bk2739 {//1431

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        for (int i = 1; i < 10; i++) {
            System.out.println(n + " * " + i + " = " + (n * i) );
        }
    }
} //1432
